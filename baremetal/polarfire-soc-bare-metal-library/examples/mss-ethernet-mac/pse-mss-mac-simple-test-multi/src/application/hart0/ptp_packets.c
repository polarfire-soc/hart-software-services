extern volatile int g_vlan_tags1;

void send_ptp_stream(mss_mac_instance_t *this_mac,
        packet_info_t *packets, int *index)
{
    uint8_t packet_buf[512];

    packet_info_t *packet = ptp_packets;
    char info_string[200];
    int count = 1;
    static int ptpv1_packets;
    static int ptpv2_packets;
    static int ethernet_packets;
    static int event_packets;
    static int uni_event_packets;
    static int ipv4_packets;
    static int ipv6_packets;
    static int mcast_packets;
    static int mcastl_packets;
    static int unicast_packets;
    static int sync_packets;
    static int announce_packets;
    static int fu_packets;
    static int drq_packets;
    static int drs_packets;
    static int pdrq_packets;
    static int pdrs_packets;
    static int pdfu_packets;
    static int signal_packets;
    static int manage_packets;
    static int vlan_tags;
    int is_internal;
    int print_stats = 1;

    uint8_t source_ip[4] = {0x0A, 0x01, 0x01, 0x02};
    uint8_t dest_ip[4]   = {0x0A, 0x01, 0x01, 0x03};

    if(g_address_swap) /* Changed address configuration... */
    {
        source_ip[3] = 0x03;
        dest_ip[3]   = 0x02;
    }

    if(packets != 0)
    {
        is_internal = 0;
        packet = &packets[(*index) - 1];
        count = *index;

        if(1 == *index)
        {
            ptpv1_packets     = 0;
            ptpv2_packets     = 0;
            ethernet_packets  = 0;
            event_packets     = 0;
            uni_event_packets = 0;
            ipv4_packets      = 0;
            ipv6_packets      = 0;
            mcast_packets     = 0;
            mcastl_packets    = 0;
            unicast_packets   = 0;
            sync_packets      = 0;
            announce_packets  = 0;
            fu_packets        = 0;
            drq_packets       = 0;
            drs_packets       = 0;
            pdrq_packets      = 0;
            pdrs_packets      = 0;
            pdfu_packets      = 0;
            signal_packets    = 0;
            manage_packets    = 0;
            vlan_tags         = 0;
            }
    }
    else
    {
        is_internal = 1;
        packet = ptp_packets;

        ptpv1_packets     = 0;
        ptpv2_packets     = 0;
        ethernet_packets  = 0;
        event_packets     = 0;
        uni_event_packets = 0;
        ipv4_packets      = 0;
        ipv6_packets      = 0;
        mcast_packets     = 0;
        mcastl_packets    = 0;
        unicast_packets   = 0;
        sync_packets      = 0;
        announce_packets  = 0;
        fu_packets        = 0;
        drq_packets       = 0;
        drs_packets       = 0;
        pdrq_packets      = 0;
        pdrs_packets      = 0;
        pdfu_packets      = 0;
        signal_packets    = 0;
        manage_packets    = 0;
        vlan_tags         = 0;
    }

    /* Swap IP address ordering if GEM1 (assuming only low octet is different) */
    if(this_mac == &g_mac1)
    {
        uint8_t addr_temp;

        addr_temp    = source_ip[3];
        source_ip[3] = dest_ip[3];
        dest_ip[3]   = addr_temp;
    }

    if(this_mac == &g_mac0) /* pick correct tags level */
    {
        vlan_tags = g_vlan_tags0;
    }
    else
    {
        vlan_tags = g_vlan_tags1;
    }

    while(packet->data != 0)
    {
        uint8_t tx_status;

        uint32_t vlan_offset = 0;
        memset(packet_buf, 0, sizeof(packet_buf));

        /* Fetch original destination MAC address */
        memcpy(&packet_buf[0], &packet->data[0], 6);

        /* Insert our own MAC address as source */
        memcpy(&packet_buf[6], this_mac->mac_addr, 6);

        /* Looking for non Multicast address */
        if((0 == (packet_buf[0] & 0x01)) || (packet->flags & PKT_IS_UCAST))
        {
            /* Insert opposing GEM MAC address for unicast packets*/
            if(this_mac == &g_mac0)
            {
                memcpy(packet_buf, g_mac1.mac_addr, 6);
            }
            else
            {
                memcpy(packet_buf, g_mac0.mac_addr, 6);
            }
        }

        if(2 == vlan_tags)
        {
            packet_buf[12] = 0x88;
            packet_buf[13] = 0xA8;
            packet_buf[14] = 0x00;
            packet_buf[15] = 0x01;

            vlan_offset = 4;
        }

        if(0 != vlan_tags)
        {
            packet_buf[12 + vlan_offset] = 0x81;
            packet_buf[13 + vlan_offset] = 0x00;
            packet_buf[14 + vlan_offset] = 0x00;
            packet_buf[15 + vlan_offset] = 0x02;

            vlan_offset += 4;
        }

        /* Fetch copy of rest of packet */
        memcpy(&packet_buf[12 + vlan_offset],
                &packet->data[12], packet->length - 12);

        if(packet->flags & PKT_IS_IPV4)
        {
            uint32_t sum;
            uint32_t csum;
            uint16_t *p_header = (uint16_t *)&packet_buf[0x0E + vlan_offset];
            int inner_index;

            /* We are always the source for IPv4 packets */
            memcpy(&packet_buf[0x1A + vlan_offset], source_ip, 4);

            /* And Unicast IPv4 packets always go to the opposing GEM */
            if(packet->flags & PKT_IS_UCAST)
                memcpy(&packet_buf[0x1E + vlan_offset], dest_ip, 4);

            /* Now rebuild the IP checksum... */

            p_header[5] = 0; /* Zero current IP checksum */
            sum = 0;
            for(inner_index = 0; inner_index != 10; inner_index++)
            {
                sum += (uint32_t)p_header[inner_index];
            }

            /* Do twice to automatically fold in any 16bit overflow */
            csum = (sum & 0xFFFF) + ((sum >> 16) & 0xFFFF);
            sum = csum;
            csum = (sum & 0xFFFF) + ((sum >> 16) & 0xFFFF);

            p_header[5] = (uint16_t)(~csum); /* Update IP checksum */
        }

        /* Want to do the packet stuffing for time stamp testing */
        if(packet->time_offset)
        {
            uint32_t size = 256 + vlan_offset;
            ts_info_t ts_blob;

            memset(&ts_blob, 0, sizeof(ts_blob));

            if(packet->flags & PKT_IS_PTPV1)
            {
                ts_blob.secs_msb    = 0;
                ts_blob.secs_lsb    = get_ptp_packet_uint(
                        &packet_buf[packet->time_offset + vlan_offset]);
                ts_blob.nanoseconds = get_ptp_packet_uint(
                        &packet_buf[packet->time_offset + 4 + vlan_offset]);
            }
            else
            {
                ts_blob.secs_msb    = (uint32_t)get_ptp_packet_short_uint(
                        &packet_buf[packet->time_offset + vlan_offset]);
                ts_blob.secs_lsb    = get_ptp_packet_uint(
                        &packet_buf[packet->time_offset + 2 + vlan_offset]);
                ts_blob.nanoseconds = get_ptp_packet_uint(
                        &packet_buf[packet->time_offset + 6 + vlan_offset]);
            }

            if(packet->correction_offset) /* Has correction field as well */
            {
                ts_blob.nsecs_msb       = (uint32_t)get_ptp_packet_short_uint(
                        &packet_buf[packet->correction_offset + vlan_offset]);
                ts_blob.nsecs_lsb       = get_ptp_packet_uint(
                        &packet_buf[packet->correction_offset + 2 + vlan_offset]);
                ts_blob.sub_nanoseconds = get_ptp_packet_uint(
                        &packet_buf[packet->correction_offset + 6 + vlan_offset]);
            }
            MSS_MAC_read_TSU(this_mac, &ts_blob.launch_time);

            memcpy(&packet_buf[size - sizeof(ts_blob)],
                    &ts_blob, sizeof(ts_blob));
            tx_status = MSS_MAC_send_pkt(this_mac, 0, packet_buf,
                    size, (void *)0);
        }
        else
        {
            tx_status = MSS_MAC_send_pkt(this_mac, 0, packet_buf,
                    packet->length + vlan_offset, (void *)0);
        }

        if(is_internal)
        {
            sprintf(info_string,"TX status = %d sending packet %d - %s\n\r",
                    (int)tx_status, count, packet->description);
        }
        else
        {
            sprintf(info_string,"TX status = %d sending packet %d - %s\n\r",
                    (int)tx_status, *index, packet->description);
        }

        PRINT_STRING(info_string);
        if(packet->flags & PKT_IS_PTPV1)
        {
            ptpv1_packets++;
        }

        if(packet->flags & PKT_IS_PTPV2)
        {
            ptpv2_packets++;
        }

        if(packet->flags & PKT_IS_ETH)
        {
            ethernet_packets++;
        }

        if(packet->flags & PKT_IS_EVENT)
        {
            event_packets++;
            if(packet->flags & PKT_IS_UCAST)
            {
                uni_event_packets++;
            }
        }

        if(packet->flags & PKT_IS_IPV4)
        {
            ipv4_packets++;
        }

        if(packet->flags & PKT_IS_IPV6)
        {
            ipv6_packets++;
        }

        if(packet->flags & PKT_IS_MCAST)
        {
            mcast_packets++;
        }

        if(packet->flags & PKT_IS_MCASTL)
        {
            mcastl_packets++;
        }

        if(packet->flags & PKT_IS_UCAST)
        {
            unicast_packets++;
        }

        if(PKT_IS_SM == (packet->flags & PKT_TYPE_MASK))
        {
            sync_packets++;
        }

        if(PKT_IS_AM == (packet->flags & PKT_TYPE_MASK))
        {
            announce_packets++;
        }

        if(PKT_IS_FUM == (packet->flags & PKT_TYPE_MASK))
        {
            fu_packets++;
        }

        if(PKT_IS_DRQM == (packet->flags & PKT_TYPE_MASK))
        {
            drq_packets++;
        }

        if(PKT_IS_DRSM == (packet->flags & PKT_TYPE_MASK))
        {
            drs_packets++;
        }

        if(PKT_IS_PDRQM == (packet->flags & PKT_TYPE_MASK))
        {
            pdrq_packets++;
        }

        if(PKT_IS_PDRSM == (packet->flags & PKT_TYPE_MASK))
        {
            pdrs_packets++;
        }

        if(PKT_IS_PDFUM == (packet->flags & PKT_TYPE_MASK))
        {
            pdfu_packets++;
        }

        if(PKT_IS_SIGM == (packet->flags & PKT_TYPE_MASK))
        {
            signal_packets++;
        }

        if(PKT_IS_MANM == (packet->flags & PKT_TYPE_MASK))
        {
            manage_packets++;
        }

        packet++;
        if(!is_internal)
        {
            if(packet->data == 0)
            {
                print_stats = 1;
                *index = 1;
            }
            else
            {
                print_stats = 0;
                *index += 1;
            }
            break;
        }
        else
        {
            count++;
        }
    }

    if(print_stats)
    {
        sprintf(info_string,"PTPv1 Packets         = % 3d PTPv2 Packets            % 3d\n\r",
                ptpv1_packets, ptpv2_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Event Packets         = % 3d Unicast Event Packets    % 3d\n\r",
                event_packets, uni_event_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Ethernet packets      = % 3d IPv4 Packets             % 3d IPv6 Packets       % 3d\n\r",
                ethernet_packets, ipv4_packets, ipv6_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Multicast packets     = % 3d Local Multicast Packets  % 3d Unicast Packets    % 3d\n\r",
                mcast_packets, mcastl_packets, unicast_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Sync packets          = % 3d Announce Packets         % 3d Follow Up Packets  % 3d\n\r",
                sync_packets, announce_packets, fu_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Delay Request packets = % 3d Delay Response Packets   % 3d\n\r",
                drq_packets, drs_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Peer Dly Req packets  = % 3d Peer Dly Resp Packets    % 3d Peer Dly follow Up % 3d\n\r",
                pdrq_packets, pdrs_packets, pdfu_packets);
        PRINT_STRING(info_string);
        sprintf(info_string,"Signaling packets     = % 3d Management Packets       % 3d\n\r",
                signal_packets, manage_packets);
        PRINT_STRING(info_string);
    }
}


uint32_t calc_gem_hash_index(uint8_t *p_address)
{
    uint64_t address;
    uint32_t index;
    uint32_t count;

    /* Create 64 bit version of 48 bit address from possibly non-aligned memory
     * location */

    /* Point to last byte of 6 byte addr as they are in low byte first order */
    p_address += 5;
    address = 0;
    for(count = 0; count != 6; count++)
    {
        address <<= 8;
        address |= (uint64_t)*p_address;
        p_address--;
    }

    index = (int32_t)address & 0x0000003Fl; /* Extract lowest 6 bits */

    /* Fold the remaining 7 x 6 bit chunks into the index */
    for(count = 0; count != 7; count++)
    {
        address >>= 6;
        index ^= (uint32_t)(address & 0x0000003Fl); /* Exor lowest 6 bits */
    }

    return(index);
}

